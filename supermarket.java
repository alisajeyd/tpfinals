    public void ItemCost(){
 
        double sum = 0;
        
        for (int i = 0; i < jTable1.getRowCount(); i++)
        {
            sum = sum + Double.parseDouble(jTable1.getValueAt(i, 2).toString());
        }
        Subtotal.setText(Double.toString(sum));
        double tutal = Double.parseDouble( Subtotal.getText());
        
        double tx = (tutal * 0.2 )/100;
        String iTaxTotal = String.format("₱ %.2f", tx);
        Tax.setText(iTaxTotal);
        
        String SubTotal = String.format("₱ %.2f", tutal);
        Subtotal.setText(SubTotal);
        
        String total = String.format("₱ %.2f", tutal + tx);
        Total.setText(total);
        
        String brcd = String.format(Total.getText());
        barcode.setText(brcd);
        
        
    }
