    public void Changee()
    {
        double sum  =0;
        double tax = 0.2;
        double cash = Double.parseDouble( jtxtDisplay.getText());
        
        for (int i = 0; i < jTable1.getRowCount(); i++)
        {
            sum = sum + Double.parseDouble(jTable1.getValueAt(i, 2).toString());
        }
        
        double tx = (sum * 0.2 )/100;
        double cChange = (cash - (sum + tx));
        
        String ChangeG = String.format("₱ %.2f", cChange);
        Change.setText(ChangeG);
    }
    
    
    
    
    
    
    
    
